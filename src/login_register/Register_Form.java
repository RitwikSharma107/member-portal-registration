    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
      
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection cn = null;
            PreparedStatement st = null;
            ResultSet rs = null;
            
            
            //Get the username and password
            String fname = jTextField_firstname.getText();
            String lname = jTextField_lastname.getText();
            String phone = jTextField_phone.getText();
            String age = jTextField_age.getText();
            String address = jTextField_address.getText();
            String street = jTextField_street.getText();
            String city = jTextField_city.getText();
            String pincode = jTextField_pincode.getText();
            //Convert date_of_birth to string
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dob = sdf.format(jDateChooser_dob.getDate());
            
            String dep = jComboBox_department.getSelectedItem().toString();
            String desig = jComboBox_designation.getSelectedItem().toString();
            
            String gender;
            if(jRadioButton_male.isSelected())
            {
                gender = "Male";
            }
            else if(jRadioButton_female.isSelected())
            {
                gender = "Female";
            }
            else
            {
                gender = "Other";
            }
            
            //Create a INSERT query to insert data on the database
            String query = "INSERT INTO regs VALUES(?,?,?,?,?,?,?,?,?,?,?,?)";
            cn = MyCon.ConnectDB();
            st = cn.prepareStatement(query);
            st.setString(1,fname);
            st.setString(2,lname);
            st.setString(3,phone);
            st.setString(4,age);
            st.setString(5,address);
            st.setString(6,street);
            st.setString(7,city);
            st.setString(8,pincode);
            st.setString(9,dob);
            st.setString(10,gender);
            st.setString(11,dep);
            st.setString(12,desig);
          
            boolean b2 = verifyFields();
            
            if(b2==true)
            {
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "You have been registered");
            
                //Progress to HomePage form
                Home_Page hp = new Home_Page();
                hp.setVisible(true);
                hp.pack();
                hp.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                //Close Login form
                this.dispose(); 
            
            
            }
            else{
                JOptionPane.showMessageDialog(null, "Retry!!!");   
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_registerActionPerformed

