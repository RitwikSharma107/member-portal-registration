    //Create a function to check for mandatory fields
    private boolean verifyFields()
    {
        String fname = jTextField_firstname.getText();
        String lname = jTextField_lastname.getText();
        String phone = jTextField_phone.getText();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dob = sdf.format(jDateChooser_dob.getDate());

        //Check empty fields
        if((fname.trim().equals("")||lname.trim().equals("")||phone.trim().equals(""))
                ||(!(jRadioButton_male.isSelected()||jRadioButton_female.isSelected()
                ||jRadioButton_other.isSelected())))
        {
            JOptionPane.showMessageDialog(null, "Mandatory fields are empty","Empty Fields",2);
            return false;
        }
        else
        {
            return true;
        }
    }
    
